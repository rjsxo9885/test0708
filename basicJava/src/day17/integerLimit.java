package day17;

public class integerLimit {

	public static void main(String[] args) {
		// 각 정수형이 푯현할수 있는 수치 범위를 표시
			
		
		System.out.println("정수형의 표현범위");
		System.out.println("byte 형(1byte):"+Byte.MIN_VALUE+"~"+Byte.MAX_VALUE);
		System.out.println("short 형(2byte):"+Short.MIN_VALUE+"~"+Short.MAX_VALUE);
		System.out.println("int 형(4 byte):"+Integer.MIN_VALUE+"~"+Integer.MAX_VALUE);
		System.out.println("long 형(8 byte):"+Long.MIN_VALUE+"~"+Long.MAX_VALUE);
		
	}

}
